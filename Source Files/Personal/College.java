package Personal;

public class College {
      private String univ;
      private String coll;
      
      public College()
      {
    	  univ=null;
    	  coll=null;
      }
      public void setuniv(String univ)
      {
    	  this.univ =univ;
      }
      public void setcoll(String coll)
      {
    	  this.coll = coll;
      }
      public String getuniv()
      {
    	  return univ;
      }
      public String getcoll()
      {
    	  return coll;
      }
      
}
